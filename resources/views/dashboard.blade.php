      function graficar1(){
          var jclients_month = <?php echo $jclients_month; ?>;
          //console.log(jclients_month);
          var speedCanvas = document.getElementById("speedChart");

          Chart.defaults.global.defaultFontFamily = "Roboto";
          Chart.defaults.global.defaultFontSize = 14;

          //Arreglos del json
          var etiquetas = [];
          var valores = [];


          for(var count=0; count< jclients_month.length; count++)
          {
              etiquetas.push(jclients_month[count].months);
              valores.push(jclients_month[count].clients);
          }


          var speedData = {
              labels: etiquetas,
              datasets: [{
                  label: "Posibles Clientes",
                  data: valores,
                  borderColor: '#2257ba'
              }]
          };

          var chartOptions = {
              legend: {
                  display: true,
                  position: 'bottom',
                  labels: {
                      boxWidth: 80,
                      fontColor: '#2257ba'
                  }
              }
          };

          var lineChart = new Chart(speedCanvas, {
              type: 'line',
              data: speedData,
              options: chartOptions
          });


      }

      function graficar2(){
